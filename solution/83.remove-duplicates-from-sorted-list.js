/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
var deleteDuplicates = function(head) {
  if (!head || !head.next) return head

  let cur = head.next;
  let prev = head;
  while (cur) {
    if (prev.val === cur.val) {
      prev.next = cur.next
    } else {
      prev = cur;
      cur = cur.next;
    }
  }
  return head
};

